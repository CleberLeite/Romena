package com.frf.app.data;


public class RankData {
    private String id;
    private String name;
    private int rank;
    private int coins;
    private String img;
    private String userType;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getRank() {
        return rank;
    }

    public int getCoins() {
        return coins;
    }

    public String getImg() {
        return img;
    }

    public String getUserType() {
        return userType;
    }
}
