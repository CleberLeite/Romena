public class NoticiaData {

    private int id;
    private String title = "";
    private String subTitle = "";
    private String img = "";
    private String dateHour = "";

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getSubTitle() {
        return subTitle;
    }

    public String getImg() {
        return img;
    }

    public String getDateHour() {
        return dateHour;
    }
}
